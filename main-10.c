/******************************************************************************

Подвиг 8. Из введенной строки отобразить 
первые пять символов в обратном порядке. Полагается, 
что введенная строка имеет минимум пять символов.

*******************************************************************************/
#include <stdio.h>

int main() {
    char str[20];
    scanf("%[^\n]", str);
    
    for (int i = 4; i >= 0; i--) {
        printf("%c", str[i]);
    }
    printf("\n");

    return 0;
}
